# Define the speed of light constant in meters per second
c = int(300000000)

# Get user input for mass (m) and convert it to an integer
m = int(input('m: '))

# Calculate the energy (E) using the formula E = m * c^2
E = m * c * c

# Print the calculated energy (E)
print('E: ', E)

